    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        try {
            String usua = Usuario.getText();
            String contra = contrasena.getText();
            Usuarios c = new Usuarios(usua, contra);
            if (admin.isSelected()) {
                c = new Usuarios(usua, contra, "Administrador");
            } else if (normal.isSelected()) {
                c = new Usuarios(usua, contra, "Normal");
            }
            GUI_Principal p = new GUI_Principal();
            p.getPusuarios().add(c);
            p.setVisible(true);
            this.setVisible(false);
        } catch (Exception e) {
            JOptionPane.showInputDialog(this, "Ocurrio un error fatal!");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1MouseClicked

